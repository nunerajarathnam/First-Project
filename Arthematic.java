
public class Arthematic
{

	public static void main(String[] args) 
	{
		//increment
		int a=2;
		int b=3;
		 a=++a;//pre increment
		b=++b;
       System.out.println(a);
		System.out.println(b);
		int c=2;
		int d=3;
		c=c++;//post increment
		d=d++;
		System.out.println(c);
		System.out.println(d);
		//decrement
		int e=2;
		int f=3;
		e -=1;//pre decrement
		f +=10;//pre increment
		System.out.println(e);
		System.out.println(f);
		}

}
