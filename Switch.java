
public class Switch 
{

	public static void main(String[] args) 
	{
		int i=6;
		switch(i)
		
		{
			case 1:
				System.out.println("One");
				break;
			case 2:
				System.out.println("TWo");
				break;
			case 3:
				System.out.println("Three");
				break;
			case 4:
				System.out.println("Four");
				break;
			case 5:
				System.out.println("Five");
				break;
			case 6:
				System.out.println("Six");
				break;
				default :
					System.out.println("No Match");
					
		}
	
	}
	
}
